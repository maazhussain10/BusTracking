Future<void> _handleSignIn(context) async {
    User? user =
    await Authentication.signInWithGoogle(context: context);

    try{
      if(user != null){

        Navigator.push(
          context,
          MaterialPageRoute(builder: (context) => FirstLogin(user: user)),
        );
      }
    }catch(e){
      print(e);
    }
}


